    def ComputeResolutions(self,   
                            fourvector : TLorentzVector , 
                            charge = +1, 
                            originPosition=(0,0,0) ,
                            PVPosition    =(0,0,0) , 
                            flyupto = -999999999.
    ) -> tuple[Matrix, Matrix]:
        unit = fourvector.Vect().Unit()
        tx = unit.X()/unit.Z()
        ty = unit.Y()/unit.Z()       
        track_props = {
            "p" : fourvector.P(),
            "pt": fourvector.Pt(),
            "eta": fourvector.Eta(),
            "phi": fourvector.Phi(),
            "charge" : charge,
            "px" : fourvector.Px(),
            "py" : fourvector.Py(),
            "pz" : fourvector.Pz(),
            "ovtx_x": originPosition[0],
            "ovtx_y": originPosition[1],
            "ovtx_z": originPosition[2],
            "pv_x": PVPosition[0],
            "pv_y": PVPosition[1],
            "pv_z": PVPosition[2],
            "mass": fourvector.Mag(),
            "tx_orig" : tx, 
            "ty_orig" : ty
        }
        pOrigin    = TVector3( track_props["ovtx_x"], track_props["ovtx_y"], track_props["ovtx_z"])
        pvPosition = TVector3( track_props["pv_x"], track_props["pv_y"], track_props["pv_z"])
        momentum = fourvector.Vect()
        unit     = fourvector.Vect().Unit()
        vOrigin  = TVector3( unit.X()/unit.Z(), unit.Y()/unit.Z(), 1.)
        Logger.debug(f"==================================================================")
        Logger.debug(f"Compute Momentum Resolution for p         = {track_props['p']:.2f} MeV")
        Logger.debug(f"Compute Momentum Resolution for originPos = {originPosition} MMDF(path, factory=None, create=True)")
        Logger.debug(f"Compute Momentum Resolution for originVec = {vOrigin}")
        if not( charge == 1. or charge == -1): 
            raise ValueError("Invalid Charge")        
        ##############################
        # Get the PV position , trac slopes at the origin of the track
        ##############################
        PVX , PVY , PVZ   = track_props["pv_x"], track_props["pv_y"], track_props["pv_z"]
        tx = track_props["tx_orig"]
        ty = track_props["ty_orig"]
        
        # Define State at the Origin of the track
        stateOrigin = TrackState( x = pOrigin.X(), y = pOrigin.Y(), z = pOrigin.Z(), tx = tx, ty=ty, qop= charge/momentum.Mag())        
        # Copy the planes to use for the tracking and p-resolution calculation
        usePlanes = [ p for p in self.planes] 
        Logger.debug(f"Number Of Planes (user)= {len(usePlanes)}")        
        # Add the storing Planes where to store output results 
        if len( self.storingPlanes) != 0 :            
            for p in self.storingPlanes :                 
                usePlanes.append(p)
        Logger.debug(f"Number Of Planes (+storing)= {len(usePlanes)}")                
        # Sort in Z the planes to use 
        usePlanes.append( 
            Plane( zval = stateOrigin.Z(), 
                thickness=0.0, dzMat=0.0, sigmaX=None, sigmaY=None, angle=None, geomLayer="any", hitefficiency=1.0, name="OriginPlane"
            )
        )
        Logger.debug(f"Number Of Planes (+originTrack)= {len(usePlanes)}")                
        usePlanes = sorted(usePlanes)
        usePlanes[0].associatedState = stateOrigin # associate the first plane to the state at the origin of the track
        # create the trajectory and extract material crossed in each step from the state Origin         
        states_in_field, curling = self.TrajectoryCreator.CreateStatesAlongTrajectory(
                stateOrigin = usePlanes[0].associatedState,
                zFinal = 13000., 
                step_size = 400., 
                noCharge = False
        )
        if curling : 
            Logger.warning("Trajectory is curling in the magnetic field, skip track")
            return
        
        Logger.debug(f"States in field to add = {len(states_in_field)}")
        nPlanes_in_field = 0 
        for idx in range(len(states_in_field)) :
            if idx == 0 : continue 
            plane_step = Plane( 
                zval = states_in_field[idx].Z(),
                thickness = 0.0, 
                dzMat = 0.0,
                angle = None, 
                sigmaX = None,
                sigmaY = None, 
                geomLayer = 'any',
                hitefficiency = 1.0,
                name = f"PlaneFromTrajectory_at_z_{states_in_field[idx].Z():.1f}"
            )
            usePlanes.append( plane_step )
            nPlanes_in_field +=1
        usePlanes = sorted( usePlanes)
        Logger.debug(f"Number Of Planes (+points in field)= {len(usePlanes)}")                        
        
        if curling :
            Logger.warning("Trajectory is curling in the magnetic field, skip track")
            return
        
        # usePlanes[0] is the origin state ! 
        for idx in range(1, len(usePlanes)):
            startState = usePlanes[idx-1].associatedState 
            stateEnd, tMat = self.TrajectoryCreator.Propagate( trState = startState, 
                                                               zOld = usePlanes[idx-1].Z, 
                                                               zNew = usePlanes[idx].Z,
                                                               noCharge=False)
            usePlanes[idx].associatedState = stateEnd
            startState = stateEnd
        Planes_Material_Crossed = []  
        nTV = 0
        nUP = 0
        usePlanes = sorted(usePlanes)        
        for idx in range(1, len(usePlanes)):
            # current to next state material found 
            startState     = usePlanes[idx-1].associatedState
            endState       = usePlanes[idx].associatedState
            Material_Crossed_In_Step = self.MatLocator.FindMaterialFromPositionToPosition( startState, endState )
            avg_tx = (startState.TX()  + endState.TX()) /2.
            avg_ty = (startState.TY()  + endState.TY()) /2.
            x_ref = startState.X()
            y_ref = startState.Y()
            z_ref = startState.Z()
            for mat in Material_Crossed_In_Step :
                # print( f"Material Found in Step = \n {mat}")
                entry_z = mat[0]
