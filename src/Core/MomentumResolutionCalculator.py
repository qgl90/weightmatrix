"""
Momentum resolution calculator (Billoir-style).

`MomentumResolutionCalculator` is the central orchestrator used by the runner:

- loads detector plane definitions from CSV (`settings["detectionlayers"]`)
- loads the B-field map and creates a `ParabolicExtrapolator`
- loads a ROOT geometry (`settings["gdmlInput"]`) and uses it to insert passive
  material planes along track steps
- propagates state/covariance through planes, applying:
  - measurement updates at measurement planes
  - multiple-scattering noise at passive material planes
- writes per-track summaries to a ROOT `TNtuple` for later comparisons

The main public API used by scripts is:

    `ComputeResolutions(fourvector, charge, originPosition, PVPosition)`

Outputs
-------
The output ROOT file contains a `TNtuple` named `ntuple` with scalar branches
generated by `GetFillingBranches(...)`.

Design note
-----------
This module currently mixes:

- settings/path handling
- detector/geometry plane building
- physics propagation and filtering
- ROOT output writing

`docs/REFACTOR_PLAN.md` proposes a decomposition into smaller classes.
"""

