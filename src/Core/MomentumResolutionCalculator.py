    def InitTupleMaking(self, outFileName : str ):         
        # baseline branches to fill
        branch_list = self.GetFillingBranches()
        # branch_list = ["charge" , "p"    , "pt" , "eta"   , "phi",
        #                 "totMat" , "nVelo", "nUT", "nDownT", 
        #                   "nFT", "nMP",
        #                 "nVeloPixel","nUTPixel", "nDownTPixel",
        #                 "RFirstMeasurementToPV"]

        branch_list += self.GetFillingBranches( plane_name= "LastMeasurement")      
        branch_list += self.GetFillingBranches( plane_name= "FirstMeasurement")      
        # branch_list += self.GetFillingBranches( plane_name= "LastPlane")      
        # branch_list += self.GetFillingBranches( plane_name= "PVPlane")      
        # branch_list += self.GetFillingBranches( plane_name= "SecondPlane")
        if len(self.storingPlanes) !=  0 : 
            #append additional branches for the 'storing' planes to keep 
            pnames_added = [] 
            for plane in self.storingPlanes:
                if plane.GetName() == "" : raise ValueError("Plane must have a name")
                branch_list += self.GetFillingBranches( plane_name= plane.GetName(), storing_plane = True)
                pnames_added.append(plane.GetName())
            if len(set(pnames_added)) != len(self.storingPlanes): 
                raise ValueError("Each storing planes must have different names! Please Fix the file")
        # Create the output File And Ntuple (via modular writer)
        if outFileName != "" : 
            schema = TupleSchema(branches=tuple(branch_list))
            self.TupleWriter = RootTupleWriter.open(
                out_file=outFileName,
                schema=schema,
                tuple_name="ntuple",
                title="p resolution for settings z",
            )
            # Keep historical attributes for compatibility with existing code paths.
            self.TFileOut = self.TupleWriter.file
            self.TNtupleOut = self.TupleWriter.tuple
        else : 
            raise ValueError("Must initialize the MomentumResolutionCalcolator with a name for the output File to store final information")
    def LoadStoringPlanes(self, csv_file_input): 
        """
        Given an external csv file , create storing planes according to keywords loaded
        """
        df = pd.read_csv(csv_file_input , comment ="#", skip_blank_lines=True)
        storing_planes = [] 
        for  z, name, geomLayer in zip( df['Z'], df['name'], df['type'].replace(" ","")):
            storing_planes.append( Plane(   zval     =float(z), 
                                            thickness=0.0, 
                                            dzMat= 0.0,
                                            sigmaX    = None, 
                                            sigmaY = None ,
                                            angle     = None,
                                            geomLayer= geomLayer,
                                            hitefficiency = 1.0,
                                            name = name  ))
        names =  [ p.GetName() for p in storing_planes]
        if len(set(names)) != len(storing_planes): raise ValueError("Planes for storing must have different names, fix the csv file!")
        return storing_planes
